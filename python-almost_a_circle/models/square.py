#!/usr/bin/python3
"""Holberton mandatory doc"""

from models.rectangle import Rectangle


class Square(Rectangle):
    """Holberton mandatory doc"""
    def __init__(self, size, x=0, y=0, id=None):
        super().__init__(size, size, x, y, id)

    @property
    def size(self):
        return self.width

    @size.setter
    def size(self, value):
        self.width = value
        self.height = value

    def __str__(self):
        return f"[Square] ({self.id}) {self.x}/{self.y} - {self.size}"

    def update(self, *args, **kwargs):
        """Hadri suce ma doc"""
        super().update(**kwargs)
        if args:
            if len(args) >= 1:
                self.id = args[0]
            if len(args) >= 2:
                self.size = args[1]
            if len(args) >= 3:
                self.x = args[2]
            if len(args) >= 4:
                self.y = args[3]
        else:
            if "size" in kwargs:
                self.size = kwargs["size"]
